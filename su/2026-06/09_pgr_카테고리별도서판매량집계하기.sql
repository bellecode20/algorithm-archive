SELECT B.CATEGORY, SUM(S.SALES)
FROM BOOK B
JOIN BOOK_SALES S
  ON B.BOOK_ID = S.BOOK_ID
WHERE DATE_FORMAT(S.SALES_DATE, '%Y-%m') = '2022-01'
GROUP BY B.CATEGORY
ORDER BY B.CATEGORY